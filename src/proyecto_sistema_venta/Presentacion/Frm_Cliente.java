    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int filaSeleccionada = TblCliente.getSelectedRow();
        if (filaSeleccionada >= 0) {   
            String id_cliente_str = String.valueOf(TblCliente.getValueAt(filaSeleccionada, 0));
            String razon_social = String.valueOf(TblCliente.getValueAt(filaSeleccionada, 3));
            String estado = String.valueOf(TblCliente.getValueAt(filaSeleccionada, 7));
            int id_cliente_int = Integer.parseInt(id_cliente_str);
            
            // Verificar si está activo o inactivo
            if (estado.equals("Activo")) {
                int respuesta = JOptionPane.showConfirmDialog(
                    this, 
                    "¿Deseas realmente DESACTIVAR el cliente: " + razon_social + "?\n(Se marcará como Inactivo y no se podrá usar en nuevas ventas)", 
                    "Desactivar Cliente", 
                    JOptionPane.YES_NO_OPTION);
            
                if (respuesta == JOptionPane.YES_OPTION) { 
                    resp = this.CONTROL.desactivar(id_cliente_int); 
                    if (resp.equalsIgnoreCase("OK")) {
                        JOptionPane.showMessageDialog(null, "Cliente " + razon_social + " DESACTIVADO correctamente.");
                        this.listar(""); 
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al desactivar el cliente: " + resp);
                    }
                }
            } else {
                int respuesta = JOptionPane.showConfirmDialog(
                    this, 
                    "¿Deseas ACTIVAR el cliente: " + razon_social + "?", 
                    "Activar Cliente", 
                    JOptionPane.YES_NO_OPTION);
            
                if (respuesta == JOptionPane.YES_OPTION) { 
                    resp = this.CONTROL.activar(id_cliente_int); 
                    if (resp.equalsIgnoreCase("OK")) {
                        JOptionPane.showMessageDialog(null, "Cliente " + razon_social + " ACTIVADO correctamente.");
                        this.listar(""); 
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al activar el cliente: " + resp);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente de la tabla para desactivar/activar.", "Error de Selección", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void cargarTiposDocumento() {
        CbxTipoDocumento.removeAllItems();
        CbxTipoDocumento.addItem("--SELECCIONE--");

        for (String t : doc.listarTiposDocumento()) {
        CbxTipoDocumento.addItem(t);
        }
        CbxTipoDocumento.setSelectedIndex(0);
    }

