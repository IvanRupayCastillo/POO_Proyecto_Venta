    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // Validar que se haya seleccionado un tipo de documento
        if (this.CbxTipoDocumento.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un tipo de documento");
            return;
        }
        
        String tipoDocumento = this.CbxTipoDocumento.getSelectedItem().toString();
        String numeroDocumento = this.TxtNumeroDocumento.getText();
        String razonSocial = this.TxtARazonSocial.getText();
        String direccion = this.TxtADireccion.getText();
        String telefono = this.TxtTelefono.getText();
        String email = this.TxtCorreo.getText();
        boolean activo = this.ChkEstado.isSelected();
   
        if (this.accion.equals("guardar")) {        
            resp = this.CONTROL.insertar(tipoDocumento, numeroDocumento, razonSocial, direccion, telefono, email);      
        } else if (this.accion.equals("editar")) {        
            resp = this.CONTROL.actualizar(this.idcliente, tipoDocumento, numeroDocumento, razonSocial, direccion, telefono, email, activo); 
        }
    
        System.out.println("Respuesta de la operación (" + this.accion + "): " + resp);   
        if (resp.equalsIgnoreCase("OK")) {
            String mensaje = (this.accion.equals("guardar"))  ? "Cliente registrado correctamente!" : "Cliente actualizado correctamente!";
            JOptionPane.showMessageDialog(null, mensaje);                           
            this.limpiar();
            this.listar(""); 
            TabCliente.setEnabledAt(0, true);
            TabCliente.setEnabledAt(1, false);
            TabCliente.setSelectedIndex(0);
        } else {        
            JOptionPane.showMessageDialog(null, "Error en la operación de " + this.accion + ": " + resp);
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

    
    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        if (TblCliente.getSelectedRowCount() == 1) {
            String id_cliente = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 0));
            String tipo_documento = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 1));
            String numero_documento = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 2));
            String razon_social = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 3));
            String direccion = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 4));
            String telefono = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 5));
            String email = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 6));
            String estado = String.valueOf(TblCliente.getValueAt(TblCliente.getSelectedRow(), 7));
            
            boolean est;
            if (estado.equals("Inactivo")) {
                est = false;
            } else {
                est = true;
            }
            this.idcliente = Integer.parseInt(id_cliente);
            this.CbxTipoDocumento.setSelectedItem(tipo_documento);
            this.TxtNumeroDocumento.setText(numero_documento);
            this.TxtARazonSocial.setText(razon_social);
            this.TxtADireccion.setText(direccion);
            this.TxtTelefono.setText(telefono);
            this.TxtCorreo.setText(email);
            this.ChkEstado.setSelected(est);
            
            TabCliente.setEnabledAt(0, false);
            TabCliente.setEnabledAt(1, true);
            TabCliente.setSelectedIndex(1);
            this.accion = "editar";
            BtnGuardar.setText("Editar");
        }else {
        JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente de la tabla para editar.", "Error de Selección", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_BtnEditarActionPerformed

